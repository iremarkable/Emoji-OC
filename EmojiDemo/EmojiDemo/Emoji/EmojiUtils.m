// convert U+XXXX to \U000XXXX
-(NSString *)convertAppleUnicodeToNormalUnicode:(NSString *)string{
    
    NSString *strUrl = [string stringByReplacingOccurrencesOfString:@"U+" withString:@""];
    long  unicodeIntValue= strtoul([strUrl UTF8String],0,16);
    UTF32Char inputChar = (UTF32Char)unicodeIntValue ;
    inputChar = NSSwapHostIntToLittle(inputChar);
    return [[NSString alloc] initWithBytes:&inputChar length:4 encoding:NSUTF32LittleEndianStringEncoding];

}

//covert [??]1F123[/??] to U+1F123
-(NSString *)convertServerSpecificFormatEmojiToStandUnicodeWithPrefix:(NSString *)prefix surfix:(NSString *)surfix string:(NSString *)string{

    NSInteger count0 = [string ff_countSubstring:prefix];
    NSInteger count1 = [string ff_countSubstring:surfix];
    NSInteger emojiCount = count0 > count1 ? count1:count0;
    
    NSString *text = [string copy];
    
    for (int i = 0; i < emojiCount ; i ++ ) {
        
        NSString * substringStart = prefix;
        NSString * substringEnd = surfix;
        
        NSRange rangeStart = [text rangeOfString:substringStart];
        NSRange rangeEnd = [text rangeOfString:substringEnd];
        
        NSInteger start = rangeStart.location + rangeStart.length;
        NSInteger end = rangeEnd.location;
        
        NSRange emojiTextRange;
        emojiTextRange.location = start;
        emojiTextRange.length = end - start;
        
        NSString *emojiText = [text substringWithRange:emojiTextRange];

        NSString *fullEmojiText =  [NSString stringWithFormat:@"%@%@%@",substringStart,emojiText,substringEnd];
        
        
        
        NSString *unicode = [NSString stringWithFormat:@"U+%@",emojiText];
        NSString *converUnicode = [self convertAppleUnicodeToNormalUnicode:unicode];
        
        text = [text ff_replaceSubString:fullEmojiText withTargetString:converUnicode];
        
    }
    
    return text;
}


